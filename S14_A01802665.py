# Write a python script that asks the user for their age and determines if they are adult or not (< 18 years old).
age = int(input("What's your age?: "))
if age >= 18:
    print("You are an adult.")
else:
    print("You are not an adult.")
# Write a python script that asks the user for their age and determines if they are a teenager or not (13-19 years old).
age = int(input("What's your age?: "))
if 13 <= age <= 19:
    print("You are a teenager.")
else:
    print("You are not a teenager.")
  
